class Command(BaseCommand):
    help = "Seed Rack and Pallet masters from the predefined list"

    def handle(self, *args, **kwargs):
        created_racks = 0
        created_pallets = 0
        for line in DATA:
            if not line.strip():
                continue
            rack_code, pallet_no = line.split()
            rack, r_created = Rack.objects.get_or_create(code=rack_code, defaults={"is_active": True})
            if r_created:
                created_racks += 1
            _, p_created = Pallet.objects.get_or_create(rack=rack, number=pallet_no, defaults={"is_active": True})
            if p_created:
                created_pallets += 1
        self.stdout.write(self.style.SUCCESS(f"Done. Racks created: {created_racks}, Pallets created: {created_pallets}"))
